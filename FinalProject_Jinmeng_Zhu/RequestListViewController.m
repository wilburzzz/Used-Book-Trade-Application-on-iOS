- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
 
    if([[segue identifier] isEqualToString:@"viewsend"]){
        UIViewController *dvc = segue.destinationViewController;
        ((ViewSendController *)dvc).request = self.selected;
        ((ViewSendController *)dvc).users = self.users;
        ((ViewSendController *)dvc).logInUser = self.logInUser;
    }
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    self.selected = (Request*)[self.logInUser.sendList.sendList objectAtIndex:indexPath.row];
    [self performSegueWithIdentifier:@"viewsend" sender:self];
}
























